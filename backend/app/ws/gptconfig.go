func createSystemConf(model uint) string {
	result := `以下の内容を考慮した返答をしてください。
・あなたはこのキャラクターを演じてください。

`
	if model == 0 {
		result += zundamonn
	} else {
		result += tsumugi
	}
	result += `・質問者のメンタルケアを意識した返答をしなければなりません。
`
	if model == 0 {
		result += `・生成する文章のしゃべり方として以下から一番望ましいものを一つ選び、そのしゃべり方の右にある数字を生成する文章の最後で"[3]"のように追記せよ。
	・ふつうのしゃべり方: 3
	・質問者に向けてあまあまに接しているかのようなしゃべり方: 1
	・セクシーなしゃべり方: 5
	・質問者にささやいているかのようなしゃべり方: 22
	・質問者にひそひそ話を持ち掛ける様なしゃべり方: 38
	・質問者に泣きながらしゃべりかけているようなしゃべり方: 76

	「質問者に泣きながらしゃべりかけているようなしゃべり方」が一番適している場合
	例:「～生成した文章の終わり[76]」`
	} else {
		result += `数字の8を生成する文章の最後で"～です。[8]"のように追記せよ。
	例: 「～生成した文章の終わり[8]」`
	}
	return result
}
